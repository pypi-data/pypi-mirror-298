def GetAllStation():
    """Get all possible station"""

    logger = logging.getLogger('ca_wls.GetAllStation')

    url = urlIwls + '/api/v1/stations'
    logger.debug(f'Requesting URL -> {url}')

    resp = requests.get(url)
    ret = resp.json()

    logger.debug(f'Number of station received: {len(ret)}' )


    return ret




