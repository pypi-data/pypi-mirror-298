def GetClosestStation(lon, lat):
    """Find the closest station"""

    logger = logging.getLogger('ca_wls.GetSerie')
    logger.debug(f'Parameters -> lon: {lon}  lat: {lat}')

    stations = GetAllStation()
    the_station = None
    the_dist = None

    for station in stations :

        dist = (abs(lon - station["longitude"])**2) + (abs(lat - station["latitude"])**2)

        # pprint.pprint(station)

        if not station.get("operating",False): continue

        if the_dist is None: the_dist = dist
        if dist <= the_dist:
            the_station = station
            the_dist = dist



    logger.debug(f'GetClosestStation found station: {the_station["officialName"]} with distance of {the_dist}')
    #pprint.pprint(theStation)

    return the_station



